class ScriptEngineProxyException extends RuntimeException
{
  public ScriptEngineProxyException()
  {
  }

  public ScriptEngineProxyException(String s)
  {
    super(s);
  }

  public ScriptEngineProxyException(String s, Throwable throwable)
  {
    super(s, throwable);
  }

  public ScriptEngineProxyException(Throwable throwable)
  {
    super(throwable);
  }
}